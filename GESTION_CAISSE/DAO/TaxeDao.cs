        public static List<Taxe> getListTaxe(String query)
        {
            NpgsqlConnection con = Connexion.Connection();
            try
            {
                List<Taxe> l = new List<Taxe>();
                NpgsqlCommand Lcmd = new NpgsqlCommand(query, con);
